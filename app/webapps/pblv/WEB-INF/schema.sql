CREATE TABLE M_USER (ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, muname VARCHAR(255) UNIQUE, mpwd VARCHAR(255), picName VARCHAR(255), sessionID VARCHAR(255));
CREATE TABLE NEWSLETTER (id NUMERIC PRIMARY KEY, text VARCHAR(1000));
CREATE TABLE M_ADMIN (ID NUMERIC PRIMARY KEY, mpwd VARCHAR(255) UNIQUE);
CREATE TABLE M_POSTS (ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, U_ID NUMERIC FOREIGN KEY REFERENCES M_USER(ID), message VARCHAR(255), private BOOLEAN);
CREATE TABLE M_FRIENDS(ID NUMERIC FOREIGN KEY REFERENCES M_USER(ID), U_ID NUMERIC FOREIGN KEY REFERENCES M_USER(ID));
CREATE TABLE M_LIKES(ID NUMERIC PRIMARY KEY,  U_ID NUMERIC FOREIGN KEY REFERENCES M_USER(ID), P_ID NUMERIC FOREIGN KEY REFERENCES M_POSTS(ID));



